class Syncify(object):
	def __init__(self, track_name, auth=None):
		self.track_name = track_name
		self.found = False
	
	def get_track_name():
		return self.track_name
	
	def is_found():
		return self.found

	def set_found(yes_or_no):
		self.found = yes_or_no
	
	def set_track_name(track_name):
		self.track_name = track_name
